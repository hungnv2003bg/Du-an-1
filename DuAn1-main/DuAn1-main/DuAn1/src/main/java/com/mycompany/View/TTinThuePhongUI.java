    private void tblTTThuePhongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTTThuePhongMouseClicked
        int row = tblTTThuePhong.getSelectedRow();
        if (row == -1) {
            return;
        }
        txtID.setText(tblTTThuePhong.getValueAt(row, 0).toString());
        txtMaTT.setText(tblTTThuePhong.getValueAt(row, 1).toString());
        txtSoNguoi.setText(tblTTThuePhong.getValueAt(row, 2).toString());
        txtNgayThue.setText(tblTTThuePhong.getValueAt(row, 3).toString());
        txtNgayTra.setText(tblTTThuePhong.getValueAt(row, 4).toString());
    }//GEN-LAST:event_tblTTThuePhongMouseClicked

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int index = tblTTThuePhong.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Chọn một dòng trên Table");
            return;
        }
        String Id = txtID.getText();
        ThongTinThuePhong tttp = getFormData();

        if (qlttthuephong.update(Id, tttp)) {
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            loadData(qlttthuephong.getAll());
            ClearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Sửa thất bại");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int index = tblTTThuePhong.getSelectedRow();
        String id = txtID.getText();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Chọn một dòng trên Table");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không ?");
        if (confirm == JOptionPane.YES_OPTION) {
            if (qlttthuephong.delete(id)) {
                JOptionPane.showMessageDialog(this, "Xóa thành công");
                loadData(qlttthuephong.getAll());
                ClearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

