    private void loadData(List<ThongTinThuePhong> listttThuePhong) {
        dtm = (DefaultTableModel) tblTTThuePhong.getModel();
        dtm.setRowCount(0);
        for (ThongTinThuePhong tttp : listttThuePhong) {
            dtm.addRow(new Object[]{
                tttp.getId(),
                tttp.getMaTT(),
                tttp.getSoNguoi(),
                tttp.getThoiGianThue(),
                tttp.getThoiGianTra(),
                tttp.getChiTietPhong(),
                tttp.getHoaDon(),
                tttp.getKhachHang()
            });
        }
    }

    private void ClearForm() {
        txtID.setText("");
        txtMaTT.setText("");
        txtSoNguoi.setText("");
        txtNgayThue.setText("");
        txtNgayTra.setText("");
    }

    private ThongTinThuePhong getFormData() {
        String maTT = txtMaTT.getText();
        String soNguoi = txtSoNguoi.getText();
        String ngayThue = txtNgayThue.getText();
        String ngayTra = txtNgayTra.getText();

        if (txtSoNguoi.getText().isEmpty()
                || txtMaTT.getText().isEmpty()
                || txtNgayThue.getText().trim().length() == 0
                || txtNgayTra.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được để trống");
            return null;
        }

        ThongTinThuePhong tttp = new ThongTinThuePhong(maTT, Date.valueOf(ngayThue), Date.valueOf(ngayTra), Integer.parseInt(soNguoi));
        return tttp;

    }

